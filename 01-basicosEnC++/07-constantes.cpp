#include<iostream>

using namespace std;

int main()
{
    //Constantes en C++
    /*Las constantes son contedores de informacion que no cambian su valor inicial*/
    const string CONSTANTE = "Esta constante no puede cambiar su valor";
    /*Primero las definimos con la paabra reservada const luego el tipo de dato que ira en ella y por ultimo el nombre que le asignaremos a la misma, se recomienda siempre nombrarlas en mayuscula como una buena practica*/

    cout<<CONSTANTE<<endl;

    return 0;
}